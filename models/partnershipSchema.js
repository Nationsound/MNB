const mongoose = require('mongoose');

const partnershipSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    trim: true
  },
  message: {
    type: String,
    required: true,
    trim: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const Partnership = mongoose.model('Partnership', partnershipSchema);

module.exports = Partnership;
